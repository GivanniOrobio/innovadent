// Function to set the language cookie for 1 year
function setLanguageCookie(lang) {
  var d = new Date();
  d.setTime(d.getTime() + (366 * 30 * 24 * 60 * 60 * 1000));
  var expires = "expires=" + d.toUTCString();
  document.cookie = "lang=" + encodeURIComponent(lang) + ";" + expires + ";path=/";
}

// Function to get a cookie value by name
function getCookie(name) {
  var nameEQ = name + "=";
  var ca = document.cookie.split(';');
  for (var i = 0; i < ca.length; i++) {
    var c = ca[i].trim();
    if (c.indexOf(nameEQ) === 0) return c.substring(nameEQ.length, c.length);
  }
  return null;
}

// Function to get language from URL query or browser language
function getLanguageFromURL() {
  const params = new URLSearchParams(window.location.search);
  return params.get('lang') || navigator.language.split('-')[0] || 'es';
}

// Determine current language: prefer cookie, otherwise from URL/browser
var currentLang = getCookie('lang') || getLanguageFromURL();
window.getCurrentLang = () => currentLang;

// On DOMContentLoaded, hide the language link that matches the current language,
// and attach event listeners for switching languages.
document.addEventListener('DOMContentLoaded', function() {
  var englishLink = document.getElementById('lang-en');
  var spanishLink = document.getElementById('lang-es');

  // Hide the link for the current language
  if (currentLang === 'en' && englishLink) {
    englishLink.style.display = 'none';
  } else if (currentLang === 'es' && spanishLink) {
    spanishLink.style.display = 'none';
  }

  // When clicking the English link, set cookie to 'en' and reload
  if (englishLink) {
    englishLink.addEventListener('click', function(e) {
      e.preventDefault();
      setLanguageCookie('en');
      location.reload();
    });
  }

  // When clicking the Spanish link, set cookie to 'es' and reload
  if (spanishLink) {
    spanishLink.addEventListener('click', function(e) {
      e.preventDefault();
      setLanguageCookie('es');
      location.reload();
    });
  }
});

